    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        List<ProduitSemiFini> produits = new ArrayList<ProduitSemiFini>();
        if (critère.equals("Sélectionner critère")) {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner le critère de recherche.", "Attention", JOptionPane.ERROR_MESSAGE);
        } else {
            if (txtSearch.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Précisez le critère de recherche.", "Attention", JOptionPane.ERROR_MESSAGE);
            } else {
                if (critère.equals("Nom")) {
                    produits = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniByName(txtSearch.getText());
                } else if (critère.equals("Densité")) {
                    produits = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniByDensity(getDaos().getDensitéDAO().getDensitéByName(txtSearch.getText()).getId());
                }
                if (produits.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Aucun produit trouvé sous le nom de " + txtSearch.getText(), "Alerte", JOptionPane.ERROR_MESSAGE);
                } else {
                    listLoad = false;
                    listDisplay(produits);
                    lstProduits.setSelectedIndex(0);
                    listDetails();
                    listLoad = true;
                }
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed
    public void buttonEnabler(boolean enabler) {
        btnEnregistrer.setEnabled(!enabler);
        btnAnnuler.setEnabled(!enabler);
        btnModifier.setEnabled(enabler);
        btnEffacer.setEnabled(enabler);
        btnPrevious.setEnabled(enabler);
        btnNext.setEnabled(enabler);
    }

    private void listDisplay(List<ProduitSemiFini> blocks) {
        ids.clear();
        listModel.clear();
        for (ProduitSemiFini prod : blocks) {
            listModel.addElement(prod.getName());
            ids.add(prod.getId());
        }
        lstProduits.setModel(listModel);
    }

    public void listDetails() {
        block = new ProduitSemiFini();
        int blockIndex = lstProduits.getSelectedIndex();
        int blockId = ids.get(blockIndex);
        block = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(blockId);
        fillIn(block);
    }

    public void fillIn(ProduitSemiFini prod) {
        txtNom.setText(prod.getName());
        txtPoids.setText(Double.toString(prod.getPoids()));
        ftxtDimension.setText(prod.getDimension().toString());
        lblDensité.setText((getDaos().getDensitéDAO().getDensitéById(prod.getDensityId()).getName()));
        txtSections.setText(Integer.toString(prod.getNbrSections()));
    }
