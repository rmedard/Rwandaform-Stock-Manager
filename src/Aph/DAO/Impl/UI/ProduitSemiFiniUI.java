    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        ProduitSemiFini produit = new ProduitSemiFini();
        listLoad = false;
        produit = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(ids.get(lstProduits.getSelectedIndex()));
        produit.setName(txtNom.getText());
        produit.setPoids(Double.parseDouble(txtPoids.getText()));
        produit.setDimension(ftxtDimension.getText());
        produit.setNbrSections(Integer.parseInt(txtSections.getText()));
        boolean updated = getDaos().getProduitSemiFiniDAO().updateProduitSemiFini(produit);
        if (updated) {
            JOptionPane.showMessageDialog(null, "Modifications efféctuées.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Opération échouée.", "Attention", JOptionPane.ERROR_MESSAGE);
        }
        listDisplay(getDaos().getProduitSemiFiniDAO().getAllProduitSemiFini());
        listLoad = true;
        lstProduits.setSelectedValue(getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(produit.getId()).getName(), true);
        lstProduits.requestFocus();
        buttonEnabler(true);
        this.getGetsBlock().refreshForm();
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void btnEffacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEffacerActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Voulez-vous éffacer le produit " + lstProduits.getSelectedValue().toString(), "Confirmez", JOptionPane.YES_NO_CANCEL_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            boolean deleted = getDaos().getProduitSemiFiniDAO().deleteproduitSemiFini(ids.get(lstProduits.getSelectedIndex()));
            if (deleted) {
                JOptionPane.showMessageDialog(null, "Suppression efféctuée", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        listLoad = false;
        listDisplay(getDaos().getProduitSemiFiniDAO().getAllProduitSemiFini());
        lstProduits.setSelectedIndex(0);
        listDetails();
        listLoad = true;
        lstProduits.requestFocus();
    }//GEN-LAST:event_btnEffacerActionPerformed

