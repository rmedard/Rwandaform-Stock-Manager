    private void btnLivrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLivrerActionPerformed
        if (txtSéctions.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Précisez combien de séctions vous voulez livrer.", "Attention", JOptionPane.ERROR_MESSAGE);
        } else if (lblDensité.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Sélectionnez le block à livrer.", "Attention", JOptionPane.ERROR_MESSAGE);
        } else if (tblBlocks.getModel().getValueAt(tblBlocks.getSelectedRow(), 3).toString().equals("-")) {
            JOptionPane.showMessageDialog(null, "Précisez le nombre de séctions de " + tblBlocks.getModel().getValueAt(tblBlocks.getSelectedRow(), 0) + " dans le stock.", "Attention", JOptionPane.ERROR_MESSAGE);
        } else if (retirantBlock.equals("Sélectionner retirant")) {
            JOptionPane.showMessageDialog(null, "Précisez le nom du retirant.", "Attention", JOptionPane.ERROR_MESSAGE);
        } else {
            BlockDélivré block = new BlockDélivré(getDaos().getTechnicienDAO().getTechnicienById(retirantIds.get(cbxBlockRetirant.getSelectedIndex() - 1)).getId(), getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(blockIds.get(tblBlocks.getSelectedRow())).getId(), Integer.parseInt(txtSéctions.getText()));
            ProduitSemiFini prod = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(block.getBlock_id());
            if (prod.getNbrSections() >= block.getNumberSections()) {
                BlockDélivré returned = getDaos().getBlockDélivréDAO().createBlockDélivré(block);
                if (returned != null) {
                    JOptionPane.showMessageDialog(null, "Livraison réussie.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Livraison échouée.", "Erreur de livraison", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Vous n'avez pas assez de " + lblDensité.getText() + " en stock.", "Attention", JOptionPane.ERROR_MESSAGE);
            }
        }
        List<ProduitSemiFini> blocks = getDaos().getProduitSemiFiniDAO().getAllProduitSemiFini();
        tableLoad = false;
        tableDisplay(blocks);
        tableLoad = true;
        cbxBlockRetirant.setSelectedIndex(0);
        lblDensité.setText("");
        buttonEnabler(true);
    }//GEN-LAST:event_btnLivrerActionPerformed

