    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        if (déstination.equals("Sélectionner déstination")) {
            JOptionPane.showMessageDialog(null, "Sélectionnez une déstination S.V.P", "Halte", JOptionPane.ERROR_MESSAGE);
        } else if (déstination.equals("Productions")) {
            productionUI.setVisible(true);
        } else if (déstination.equals("Densités")) {
            densitéUI.setVisible(true);
        } else if (déstination.equals("Produits semi-finis")) {
            produitSemiFiniUI.setVisible(true);
            produitSemiFiniUI.setGetsBlock(this);
        } else if (déstination.equals("Techniciens")) {
            technicienUI.setVisible(true);
        } else if (déstination.equals("Quitter")) {
            int i = JOptionPane.showConfirmDialog(null, "Voulez-vous quitter l'application?", "Confirmer", JOptionPane.YES_NO_CANCEL_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

