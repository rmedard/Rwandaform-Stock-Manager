    public void tableDisplay(List<ProduitSemiFini> blocks) {
        columnNamesStock = new Object[tblBlocks.getColumnCount()];
        for (int i = 0; i < tblBlocks.getColumnCount(); i++) {
            columnNamesStock[i] = tblBlocks.getColumnName(i);
        }
        blockDetails = new Object[blocks.size()][tblBlocks.getColumnCount()];

        for (int i = 0; i < blocks.size(); i++) {
            blockDetails[i][0] = getDaos().getDensitéDAO().getDensitéById(blocks.get(i).getDensityId()).getName();
            blockDetails[i][1] = blocks.get(i).getPoids();
            blockDetails[i][2] = blocks.get(i).getDimension();
            if (!Integer.toString(blocks.get(i).getNbrSections()).equals("NULL")) {
                blockDetails[i][3] = blocks.get(i).getNbrSections();
            } else {
                blockDetails[i][3] = "-";
            }
            blockIds.add(blocks.get(i).getId());
        }
        tblBlocks.setModel(new DefaultTableModel(blockDetails, columnNamesStock));
    }

    public void tableDetails() {
        ProduitSemiFini produit = null;
        int selectedRow = tblBlocks.getSelectedRow();

        try {
            if (selectedRow != -1) {
                produit = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(blockIds.get(tblBlocks.getSelectedRow()));
            } else {
                produit = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(blockIds.get(0));
            }
        } catch (NullPointerException e) {
            produit = getDaos().getProduitSemiFiniDAO().getProduitSemiFiniById(blockIds.get(0));
        }
        lblDensité.setText(produit.getName());
    }

    public void refreshForm() {
        List<ProduitSemiFini> blocks = new ArrayList<ProduitSemiFini>();
        blocks = getDaos().getProduitSemiFiniDAO().getAllProduitSemiFini();
        tableDisplay(blocks);
    }
