    private void lstTechniciensValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstTechniciensValueChanged
        if (listLoad == true) {
            listDetails();
        }
    }//GEN-LAST:event_lstTechniciensValueChanged
    public void fillIn(Technicien tech) {
        txtNom.setText(tech.getLastName());
        txtPrénom.setText(tech.getFirstName());
        txtNumTel.setText(tech.getNumTel());
    }

    public void listDisplay(List<Technicien> techns) {
        int i = 1;
        ids.clear();
        listmodel.clear();
        for (Technicien techn : techns) {
            listmodel.addElement(i + "." + techn.getLastName().concat(" " + techn.getFirstName()));
            ids.add(techn.getId());
            ++i;
        }
        lstTechniciens.setModel(listmodel);
    }

    public void listDetails() {
        technicien = new Technicien();
        int techIndex = lstTechniciens.getSelectedIndex();
        int techId = ids.get(techIndex);
        technicien = techDAOImpl.getTechnicienById(techId);
        fillIn(technicien);
    }

