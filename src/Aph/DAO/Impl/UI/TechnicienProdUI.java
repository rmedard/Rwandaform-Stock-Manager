    private void btnChercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChercherActionPerformed
        listLoad = false;
        List<Technicien> techn = new ArrayList<Technicien>();
        if (!searchCriterion.equals("Sélectionner critère")) {
            if (searchCriterion.equals("Nom")) {
                techn = techDAOImpl.getTechnicienByLastName(txtSearch.getText());
            } else if (searchCriterion.equals("Prénom")) {
                techn = techDAOImpl.getTechnicienByFirstName(txtSearch.getText());
            } else if (searchCriterion.equals("Numéro de téléphone")) {
                techn.add(techDAOImpl.getTechnicienByPhoneNum(txtSearch.getText()));
            }
            if (techn.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Aucun technicien trouvé.", "Attention", JOptionPane.ERROR_MESSAGE);
            } else {
                listDisplay(techn);
                lstTechniciens.setSelectedIndex(0);
                listDetails();
                listLoad = true;
                lstTechniciens.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Précisez le critère de recherche.", "Attention", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnChercherActionPerformed

