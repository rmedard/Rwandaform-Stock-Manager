    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        listLoad = false;
        Technicien tech;
        if (saveOrUpdate) {
            tech = new Technicien(txtNom.getText(), txtPrénom.getText(), txtNumTel.getText());
            techDAOImpl.createTechnicien(tech);
            JOptionPane.showMessageDialog(null, "Enregistrement effectué", "Enregistrement", JOptionPane.INFORMATION_MESSAGE);
        } else {
            tech = techDAOImpl.getTechnicienById(technicien.getId());
            tech.setLastName(txtNom.getText());
            tech.setFirstName(txtPrénom.getText());
            tech.setNumTel(txtNumTel.getText());
            boolean updated = techDAOImpl.updateTechnicien(tech);
            if (updated) {
                JOptionPane.showMessageDialog(null, "Modification effectuée", "Modification", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        listDisplay(techDAOImpl.getAllTechniciens());
        listLoad = true;
        lstTechniciens.setSelectedValue(tech.getLastName() + " " + tech.getFirstName(), true);
        lstTechniciens.requestFocus();
        buttonEnabler(true);
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void btnEffacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEffacerActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer le technicien: " + txtNom.getText(), "Confirmer", JOptionPane.YES_NO_CANCEL_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            boolean deleted = techDAOImpl.deleteTechnicien(ids.get(lstTechniciens.getSelectedIndex()));
            if (deleted) {
                JOptionPane.showMessageDialog(null, "Opération effectuée", "Suppression", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        listLoad = false;
        listDisplay(techDAOImpl.getAllTechniciens());
        lstTechniciens.setSelectedIndex(0);
        listDetails();
        listLoad = true;
        lstTechniciens.requestFocus();
        buttonEnabler(true);
    }//GEN-LAST:event_btnEffacerActionPerformed

