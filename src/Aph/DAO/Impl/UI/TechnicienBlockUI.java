    private void btnChercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChercherActionPerformed
        listLoad = false;
        if (searchCriterion.equals("Nom")) {
            List<Technicien> techn = getDaos().getTechnicienDAO().getTechnicienByLastName(txtSearch.getText());
            listDisplay(techn);
        } else if (searchCriterion.equals("Prénom")) {
            List<Technicien> techn = getDaos().getTechnicienDAO().getTechnicienByFirstName(txtSearch.getText());
            listDisplay(techn);
        } else if (searchCriterion.equals("Numéro de téléphone")) {
            List<Technicien> techn = new ArrayList<Technicien>();
            techn.add(getDaos().getTechnicienDAO().getTechnicienByPhoneNum(txtSearch.getText()));
            listDisplay(techn);
        }
        lstTechniciens.setSelectedIndex(0);
        listDetails();
        listLoad = true;
        lstTechniciens.requestFocus();
    }//GEN-LAST:event_btnChercherActionPerformed

    private void btnEffacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEffacerActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer le technicien: " + txtNom.getText(), "Confirmer", JOptionPane.YES_NO_CANCEL_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            boolean deleted = getDaos().getTechnicienDAO().deleteTechnicien(ids.get(lstTechniciens.getSelectedIndex()));
            if (deleted) {
                JOptionPane.showMessageDialog(null, "Opération effectuée", "Suppression", JOptionPane.INFORMATION_MESSAGE);
                listLoad = false;
                listDisplay(getDaos().getTechnicienDAO().getAllTechniciens());
                lstTechniciens.setSelectedIndex(0);
                listDetails();
                listLoad = true;
                lstTechniciens.requestFocus();
                buttonEnabler(true);
            }
        }
    }//GEN-LAST:event_btnEffacerActionPerformed

