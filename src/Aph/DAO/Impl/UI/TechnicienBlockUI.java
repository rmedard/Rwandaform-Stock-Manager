    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        listLoad = false;
        Technicien tech;
        if (saveOrUpdate) {
            tech = new Technicien(txtNom.getText(), txtPrénom.getText(), txtNumTel.getText());
            getDaos().getTechnicienDAO().createTechnicien(tech);
            JOptionPane.showMessageDialog(null, "Enregistrement effectué", "Enregistrement", JOptionPane.INFORMATION_MESSAGE);
        } else {
            tech = getDaos().getTechnicienDAO().getTechnicienById(technicien.getId());
            tech.setLastName(txtNom.getText());
            tech.setFirstName(txtPrénom.getText());
            tech.setNumTel(txtNumTel.getText());
            boolean updated = getDaos().getTechnicienDAO().updateTechnicien(tech);
            if (updated) {
                JOptionPane.showMessageDialog(null, "Modification effectuée", "Modification", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        listDisplay(getDaos().getTechnicienDAO().getAllTechniciens());
        listLoad = true;
        lstTechniciens.setSelectedValue(tech.getLastName() + " " + tech.getFirstName(), true);
        lstTechniciens.requestFocus();
        buttonEnabler(true);
    }//GEN-LAST:event_btnEnregistrerActionPerformed

