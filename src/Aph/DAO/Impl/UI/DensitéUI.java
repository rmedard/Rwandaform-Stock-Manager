    private void btnEffacerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEffacerActionPerformed
        int i = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer la densité " + getDaos().getDensitéDAO().getDensitéById(ids.get(lstDensités.getSelectedIndex())).getName(), "Confirmez", JOptionPane.YES_NO_CANCEL_OPTION);
        if (i == JOptionPane.YES_OPTION) {
            boolean deleted = getDaos().getDensitéDAO().deleteDensité(getDaos().getDensitéDAO().getDensitéById(ids.get(lstDensités.getSelectedIndex())).getId());
            if (deleted) {
                JOptionPane.showMessageDialog(null, "Suppression efféctuée.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEffacerActionPerformed

    public void listDisplay(List<Densité> densités) {
        ids.clear();
        listModel.clear();
        for (Densité dens : densités) {
            listModel.addElement(dens.getName());
            ids.add(dens.getId());
        }
        lstDensités.setModel(listModel);
    }

    public void listDetails() {
        densité = new Densité();
        int densIndex = lstDensités.getSelectedIndex();
        int densId = ids.get(densIndex);
        densité = getDaos().getDensitéDAO().getDensitéById(densId);
        fillIn(densité);
    }

    public void fillIn(Densité dens) {
        columnNames = new Object[2];
        columnNames[0] = tblConsom.getColumnName(0);
        columnNames[1] = tblConsom.getColumnName(1);
        txtNom.setText(dens.getName());
        tblConsom.setModel(new DefaultTableModel(dens.getConsommation(), columnNames));
    }

    public void buttonEnabler(boolean enabler) {
        btnAjouter.setEnabled(enabler);
        btnModifier.setEnabled(enabler);
        btnEffacer.setEnabled(enabler);
        btnEnregistrer.setEnabled(!enabler);
        btnAnnuler.setEnabled(!enabler);
        tblConsom.setEnabled(!enabler);
        txtNom.setEditable(!enabler);
    }
