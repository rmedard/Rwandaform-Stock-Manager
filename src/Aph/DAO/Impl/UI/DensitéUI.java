    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        buttonEnabler(false);
        txtNom.setText("");
        for (int i = 0; i < tblConsom.getRowCount(); i++) {
            tblConsom.setValueAt("", i, 1);
        }
        saveOrUpdate = true;
        txtNom.requestFocus();
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnregistrerActionPerformed
        Densité dens = new Densité();
        listLoad = false;
        Object[][] consom = new Object[tblConsom.getRowCount()][2];
        for (int i = 0; i < tblConsom.getRowCount(); i++) {
          //  consom[i][0] = tblConsom.getModel().setValueAt(, WIDTH, currentRow)
            consom[i][1] = tblConsom.getValueAt(i, 1);
        }
        if (saveOrUpdate) {
            dens = new Densité(txtNom.getText(), consom);
            getDaos().getDensitéDAO().createDensité(dens);
            JOptionPane.showMessageDialog(null, "Enregistrement efféctué", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
        } else {
            dens = getDaos().getDensitéDAO().getDensitéById(ids.get(lstDensités.getSelectedIndex()));
            dens.setName(txtNom.getText());
            dens.setConsommation(consom);
            dens.setConsRowNum(tblConsom.getRowCount());
            boolean updated = getDaos().getDensitéDAO().updateDensité(densité, null);
            if (updated) {
                JOptionPane.showMessageDialog(null, "Modification efféctuée.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            }
        }
        listDisplay(getDaos().getDensitéDAO().getAllDensité());
        listLoad = true;
        lstDensités.setSelectedValue(dens.getName(), true);
        lstDensités.requestFocus();
        buttonEnabler(true);
    }//GEN-LAST:event_btnEnregistrerActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        List<Densité> allDensités = getDaos().getDensitéDAO().getAllDensité();
        listDisplay(allDensités);
        listLoad = false;
        lstDensités.setSelectedIndex(0);
        listDetails();
        listLoad = true;
        lstDensités.requestFocus();
        buttonEnabler(true);
    }//GEN-LAST:event_formWindowOpened

    private void lstDensitésValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstDensitésValueChanged
        if (listLoad == true) {
            listDetails();
        }
    }//GEN-LAST:event_lstDensitésValueChanged

